package modelo.clasesParaDatos;

public class Rango {
	
	private int minimo;
	private int maximo;
	
	public Rango(int minimo, int maximo) {
		super();
		this.minimo = minimo;
		this.maximo = maximo;
	}

	public int getMinimo() {
		return minimo;
	}

	public int getMaximo() {
		return maximo;
	}
	
	

}
